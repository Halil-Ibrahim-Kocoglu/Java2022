
public class FourTransaction {
	public int Plus(int num1,int num2) {
		return num1+num2;
	}
	public int Minus(int num1,int num2) {
		return num1-num2;
	}
	public int Multiply(int num1,int num2) {
		return num1*num2;
	}
	public double Divide(double num1,double num2) {
		return num1/num2;
	}	
}
